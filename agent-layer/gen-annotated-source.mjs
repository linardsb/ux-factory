// gen-annotated-source.mjs — annotated-source snippets: real code regions extracted from the
// live source files (docs/epics/annotated-source-glossary.architecture.md; PRD §6 legibility).
// Reads agent-layer/annotated-source.spec.json (hand-authored: file + unique anchor strings +
// the author's prose), resolves each region against the live source and emits
// system/annotated-source.json — so the code shown on approach.html can never drift from the
// code that runs. A missing or ambiguous anchor is a HARD failure — that is the drift guard.
// Determinism is mandatory: the drift-check gate re-runs this in check mode (no timestamps,
// spec order preserved, trailing newline).
// Standalone:  node agent-layer/gen-annotated-source.mjs [--check]
// Paths resolve from this module (NOT cwd) — build.mjs runs from the jobs folder.

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const SPEC = "agent-layer/annotated-source.spec.json";
const DEST = "system/annotated-source.json";

// Validate by hand at the boundary (portal/lib/intake.mjs posture) — the spec is hand-authored,
// so it is the input most likely to be wrong. Throw plain Errors naming the offending path.
function loadSpec() {
  const spec = JSON.parse(readFileSync(join(ROOT, SPEC), "utf8"));
  if (!Array.isArray(spec.snippets) || !spec.snippets.length)
    throw new Error(`${SPEC}: snippets must be a non-empty array`);
  const ids = new Set();
  spec.snippets.forEach((s, i) => {
    for (const key of ["id", "file", "anchorStart", "anchorEnd", "title"])
      if (typeof s[key] !== "string" || !s[key])
        throw new Error(`${SPEC}: snippets[${i}].${key} must be a non-empty string`);
    if (!Array.isArray(s.prose) || !s.prose.length || s.prose.some((p) => typeof p !== "string" || !p))
      throw new Error(`${SPEC}: snippets[${i}].prose must be a non-empty array of non-empty strings`);
    if (ids.has(s.id)) throw new Error(`${SPEC}: duplicate snippet id "${s.id}"`);
    ids.add(s.id);
  });
  return spec;
}

// Resolve an anchor to its single matching line index (substring match). Zero or many matches
// mean the exhibited region can no longer be identified — hard-fail rather than guess.
function anchorLine(lines, anchor, file) {
  const hits = [];
  lines.forEach((line, i) => { if (line.includes(anchor)) hits.push(i); });
  if (hits.length !== 1)
    throw new Error(`annotated-source: anchor "${anchor}" matched ${hits.length} lines in ${file} — fix ${SPEC}`);
  return hits[0];
}

// Emit the artifact (or, with {check: true}, compare against disk and report drift).
// Returns { snippets (count), drifted (file names, check mode only) }.
export function genAnnotatedSource({ check = false } = {}) {
  const spec = loadSpec();
  const snippets = spec.snippets.map((s) => {
    const lines = readFileSync(join(ROOT, s.file), "utf8").split("\n");
    const start = anchorLine(lines, s.anchorStart, s.file);
    const end = anchorLine(lines, s.anchorEnd, s.file);
    if (end < start)
      throw new Error(`annotated-source: anchorEnd "${s.anchorEnd}" precedes anchorStart "${s.anchorStart}" in ${s.file} — fix ${SPEC}`);
    return {
      id: s.id,
      file: s.file,
      startLine: start + 1,
      endLine: end + 1,
      title: s.title,
      prose: s.prose,
      code: lines.slice(start, end + 1).join("\n"),
    };
  });
  const out = JSON.stringify(
    {
      $description:
        "GENERATED by agent-layer/gen-annotated-source.mjs from agent-layer/annotated-source.spec.json — do not edit. Code fields are extracted verbatim from the named files; regenerate after any edit inside an anchored region.",
      generatedFrom: SPEC,
      snippets,
    },
    null,
    2
  ) + "\n";
  if (check)
    return { snippets: snippets.length, drifted: readFileSync(join(ROOT, DEST), "utf8") !== out ? [DEST] : [] };
  writeFileSync(join(ROOT, DEST), out);
  return { snippets: snippets.length, drifted: [] };
}

// pathToFileURL, not `file://${argv[1]}`: this repo's path contains a space, which
// import.meta.url percent-encodes — the naive comparison never matches.
if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  const check = process.argv.includes("--check");
  const r = genAnnotatedSource({ check });
  if (check && r.drifted.length) {
    console.error(`annotated source ✗  drift from live source: ${r.drifted.join(", ")} — regenerate with: node agent-layer/gen-annotated-source.mjs`);
    process.exit(1);
  }
  console.log(`annotated source ✓  ${r.snippets} snippets ${check ? "— no drift" : `(${DEST})`}`);
}
