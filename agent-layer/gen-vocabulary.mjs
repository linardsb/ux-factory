// gen-vocabulary.mjs — the design system made consumable by agents (epic #1, ticket #11).
// Spec: docs/epics/ai-first-ux-factory.architecture.md §Agentic UI (lines 51–57).
// Projects the six committed ComponentSpec heads + Usage prose + inlined DataContracts into
// ONE name-keyed file an agent can be prompted with: names, prop schemas, composition rules,
// usage guidance, and each component's data shape. This is what system/agentic-renderer.mjs
// validates compositions against; it also ships in the handoff pack as the "agent-ready" layer.
// Emits handoff/verdant/vocabulary.json — a sibling of gen-handoff.mjs's pack.json, generated
// from the same source in the same build so the two cannot drift.
// Standalone:  node agent-layer/gen-vocabulary.mjs
// Paths resolve from this module (NOT cwd) — build.mjs runs from the jobs folder.

import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";
import { parseComponentSpec } from "./lib.mjs";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const SPECS = join(ROOT, "system/specs");
const DEST = join(ROOT, "handoff/verdant");

export function genVocabulary() {
  const files = readdirSync(SPECS).filter((f) => f.endsWith(".md")).sort();
  const specs = files.map((f) => parseComponentSpec(join(SPECS, f)));

  const components = {};
  for (const { head, sections, path } of specs) {
    // Contract inlined verbatim (agents need the data shape in the one file), or null.
    const contract = head.contract
      ? JSON.parse(readFileSync(join(dirname(path), head.contract), "utf8"))
      : null;
    const usage = sections.find((s) => s.title === "Usage")?.body ?? "";
    components[head.component] = {
      class: head.class,
      status: head.status,
      props: head.props,
      states: head.states,
      children: head.children,
      usage,
      contract,
    };
  }

  const vocab = {
    $description:
      "Verdant component vocabulary — the design system made consumable by agents: component names, prop schemas, composition rules, usage guidance, and each component's DataContract inlined. Verdant is a fictional demo scenario. Generated by agent-layer/gen-vocabulary.mjs from system/specs/ — do not edit.",
    scenario: "verdant",
    generatedFrom: "system/specs",
    composition: {
      shape: "a composition is one node or an array of nodes; a node is {name, props, children?}",
      childrenRule:
        "a node may carry at most one child, only when its vocabulary entry lists allowed children, and the child's name must be in that list",
      chipRule:
        "plant-card and care-task-row always render one status-chip derived from their own status prop; supply an explicit status-chip child only to override its label, and its value must equal the parent's status",
    },
    components,
  };

  // mkdir so a standalone run works on a fresh clone even before gen-handoff has populated the pack.
  mkdirSync(DEST, { recursive: true });
  writeFileSync(join(DEST, "vocabulary.json"), JSON.stringify(vocab, null, 2) + "\n");

  return { components: specs.length, dest: DEST };
}

// pathToFileURL, not `file://${argv[1]}`: this repo's path contains a space, which
// import.meta.url percent-encodes — the naive comparison never matches.
if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  const r = genVocabulary();
  console.log(`vocabulary      ✓  ${r.components} components (handoff/verdant/vocabulary.json)`);
}
