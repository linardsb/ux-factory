// gen-loc-summary.mjs — the measured size of the shipped surface (PRD §6 legibility: "small
// enough to read" as a drift-checked artifact, not prose — the number on approach.html is this
// file's output, never hand-written). Counts tracked files (git ls-files — the same source of
// truth tooling/drift-check.mjs's syntax step uses) in three shipped groups and emits
// system/loc-summary.json. Line counts are ROUNDED to the nearest 100 on purpose: the claim
// stays honest ("≈") without the drift gate churning on every ordinary edit.
// Determinism is mandatory: the drift-check gate re-runs this in check mode (no timestamps,
// fixed group order, sorted file list, trailing newline).
// Standalone:  node agent-layer/gen-loc-summary.mjs [--check]
// Paths resolve from this module (NOT cwd) — build.mjs runs from the jobs folder.

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";
import { execFileSync } from "node:child_process";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const DEST = "system/loc-summary.json";

// Group membership is explicit regex, not pathspec glob (git's `*` crosses slashes — too easy
// to over-match). Order here is the emitted order.
const GROUPS = [
  { id: "runtime",    label: "design system (system/ — tokens, components, view-time modules)", test: (p) => /^system\/(wc\/)?[^/]+\.(css|mjs|js)$/.test(p) },
  { id: "pages",      label: "shipped pages (root + proto/)",                                   test: (p) => /^(?:[^/]+|proto\/[^/]+)\.html$/.test(p) },
  { id: "generators", label: "build-time generators (agent-layer/)",                            test: (p) => /^agent-layer\/[^/]+\.mjs$/.test(p) },
];

const round100 = (n) => Math.round(n / 100) * 100;

// Emit the artifact (or, with {check: true}, compare against disk and report drift).
// Returns { groups (count), drifted (file names, check mode only) }.
export function genLocSummary({ check = false } = {}) {
  const tracked = execFileSync("git", ["ls-files"], { cwd: ROOT, encoding: "utf8" })
    .split("\n")
    .filter(Boolean)
    .sort();
  let totalFiles = 0;
  let totalLines = 0;
  const groups = GROUPS.map((g) => {
    const files = tracked.filter(g.test);
    if (!files.length) throw new Error(`loc-summary: group "${g.id}" matched no tracked files — fix agent-layer/gen-loc-summary.mjs`);
    const lines = files.reduce((sum, f) => sum + readFileSync(join(ROOT, f), "utf8").split("\n").length, 0);
    totalFiles += files.length;
    totalLines += lines;
    return { id: g.id, label: g.label, files: files.length, linesApprox: round100(lines) };
  });
  const out = JSON.stringify(
    {
      $description:
        "GENERATED by agent-layer/gen-loc-summary.mjs — do not edit. Counted from the tracked source (git ls-files); linesApprox is rounded to the nearest 100 so the number stays honest without churning on every edit. Regenerate: node agent-layer/gen-loc-summary.mjs",
      groups,
      total: { files: totalFiles, linesApprox: round100(totalLines) },
    },
    null,
    2
  ) + "\n";
  if (check) {
    let prior;
    try { prior = readFileSync(join(ROOT, DEST), "utf8"); } catch { prior = ""; }
    return { groups: groups.length, drifted: prior !== out ? [DEST] : [] };
  }
  writeFileSync(join(ROOT, DEST), out);
  return { groups: groups.length, drifted: [] };
}

// pathToFileURL, not `file://${argv[1]}`: this repo's path contains a space, which
// import.meta.url percent-encodes — the naive comparison never matches.
if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  const check = process.argv.includes("--check");
  const r = genLocSummary({ check });
  if (check && r.drifted.length) {
    console.error(`loc summary ✗  drift from tracked source: ${r.drifted.join(", ")} — regenerate with: node agent-layer/gen-loc-summary.mjs`);
    process.exit(1);
  }
  console.log(`loc summary ✓  ${r.groups} groups ${check ? "— no drift" : `(${DEST})`}`);
}
