// gen-system-graph.mjs — the shipped token contract's measured shape (portfolio-ux-uplift
// §Phase 5). Scans the DTCG contract group (via gen-token-css's loadSource), the 28
// components.css blocks (which tokens each consumes), and the three committed packs
// (what each binds per token, quoted VERBATIM — neutral's var(--color-ink) alias text is
// the honest raw value, never resolved by hand) into system/system-graph.json. The
// factory.html "shape of the system" exhibit renders this artifact — a token↔consumer
// graph is never hand-drawn. Drift-checked by tooling/drift-check.mjs.
// Determinism is mandatory: fixed order (source order for tokens, file order for blocks),
// no timestamps, trailing newline.
// Standalone:  node agent-layer/gen-system-graph.mjs [--check]
// Paths resolve from this module (NOT cwd) — same rule as every sibling generator.

import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";
import { loadSource } from "./gen-token-css.mjs";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const DEST = "system/system-graph.json";
const PACK_FILES = {
  neutral: "system/tokens.neutral.css",
  saulera: "system/tokens.saulera.css",
  verdant: "system/tokens.verdant.css",
};

const isToken = (node) => node && typeof node === "object" && "$value" in node;
const slug = (s) => s.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");

// Every `--name: value;` declaration in a pack file → { "--name": "raw value" }.
function packDeclarations(rel) {
  const css = readFileSync(join(ROOT, rel), "utf8");
  const decls = {};
  for (const m of css.matchAll(/^\s*(--[a-z0-9-]+)\s*:\s*([^;]+);/gm)) decls[m[1]] = m[2].trim();
  return decls;
}

// Emit the artifact (or, with {check: true}, compare against disk and report drift).
// Returns { counts, drifted (check mode only) }.
export function genSystemGraph({ check = false } = {}) {
  // 1. The contract's leaf tokens, grouped by their source section (fg-surface, motion, …).
  const src = loadSource();
  const packs = Object.fromEntries(
    Object.entries(PACK_FILES).map(([pack, rel]) => [pack, packDeclarations(rel)])
  );
  const tokens = [];
  for (const [section, sec] of Object.entries(src.contract).filter(([k]) => !k.startsWith("$"))) {
    for (const [name, tok] of Object.entries(sec)) {
      if (!isToken(tok)) continue;
      const cssName = `--${name}`;
      tokens.push({
        name: cssName,
        group: section,
        packs: Object.fromEntries(
          Object.keys(PACK_FILES).map((p) => [p, packs[p][cssName] ?? null])
        ),
      });
    }
  }
  if (!tokens.length) throw new Error("system-graph: zero contract tokens in system/tokens.source.json");
  const contractNames = new Set(tokens.map((t) => t.name));

  // 2. Consumers: the components.css blocks (delimited /* ---------- Name ---------- */),
  // each with the DISTINCT contract tokens it references. token-lint's UNDECLARED check
  // already guarantees every components.css var() is a contract token; the filter here
  // keeps that relation structural in the artifact too.
  const css = readFileSync(join(ROOT, "system/components.css"), "utf8");
  const headerRe = /^\/\* -{5,} (.+?) -{5,} \*\/$/gm;
  const headers = [...css.matchAll(headerRe)];
  if (!headers.length) throw new Error("system-graph: zero block headers in system/components.css");
  const consumers = [];
  const seenIds = new Set();
  headers.forEach((h, i) => {
    const body = css.slice(h.index + h[0].length, i + 1 < headers.length ? headers[i + 1].index : css.length);
    const specMatch = /\((system\/specs\/[^)]+\.md)\)/.exec(h[1]);
    const label = h[1].replace(/\s*\(system\/specs\/[^)]+\.md\)/, "").trim();
    const id = slug(label);
    if (seenIds.has(id)) throw new Error(`system-graph: duplicate block id "${id}" in system/components.css`);
    seenIds.add(id);
    const used = new Set();
    for (const m of body.matchAll(/var\(\s*(--[a-z0-9-]+)/g))
      if (contractNames.has(m[1])) used.add(m[1]);
    if (!used.size) return; // structural-only block — no token edge to draw
    consumers.push({ id, label, spec: specMatch ? specMatch[1] : null, tokens: [...used] });
  });
  if (!consumers.length) throw new Error("system-graph: zero token-consuming blocks in system/components.css");

  const counts = {
    tokens: tokens.length,
    consumers: consumers.length,
    edges: consumers.reduce((sum, c) => sum + c.tokens.length, 0),
  };
  const out = JSON.stringify(
    {
      $description:
        "GENERATED by agent-layer/gen-system-graph.mjs — do not edit. The shipped token contract's measured shape: contract tokens (system/tokens.source.json) × consumer blocks (system/components.css) × the three committed packs' raw declared values (quoted verbatim, aliases unresolved). Regenerate: node agent-layer/gen-system-graph.mjs",
      tokens,
      consumers,
      counts,
    },
    null,
    2
  ) + "\n";
  if (check) {
    let prior;
    try { prior = readFileSync(join(ROOT, DEST), "utf8"); } catch { prior = ""; }
    return { counts, drifted: prior !== out ? [DEST] : [] };
  }
  writeFileSync(join(ROOT, DEST), out);
  return { counts, drifted: [] };
}

// pathToFileURL, not `file://${argv[1]}`: this repo's path contains a space, which
// import.meta.url percent-encodes — the naive comparison never matches.
if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  const check = process.argv.includes("--check");
  const r = genSystemGraph({ check });
  if (check && r.drifted.length) {
    console.error(`system graph ✗  drift: ${r.drifted.join(", ")} — regenerate with: node agent-layer/gen-system-graph.mjs`);
    process.exit(1);
  }
  console.log(`system graph ✓  ${r.counts.tokens} tokens · ${r.counts.consumers} consumers · ${r.counts.edges} edges ${check ? "— no drift" : `(${DEST})`}`);
}
